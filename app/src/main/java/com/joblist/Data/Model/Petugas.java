package com.joblist.Data.Model;

public class Petugas {
    String id_petugas;
    String username;
    String password;
    String nama_petugas;
    String level;

    public String getId_petugas() {
        return id_petugas;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getNama_petugas() {
        return nama_petugas;
    }

    public String getLevel() {
        return level;
    }
}
